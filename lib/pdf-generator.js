import jsPDF from "jspdf"
import { autoTable } from "jspdf-autotable"

// Color palette for professional look
const COLORS = {
  primary: [31, 73, 125],      // Dark blue
  secondary: [79, 129, 189],   // Medium blue
  accent: [155, 194, 230],     // Light blue
  success: [70, 136, 71],      // Green
  warning: [255, 192, 0],      // Orange
  danger: [192, 57, 43],       // Red
  text: [33, 37, 41],          // Dark gray
  lightGray: [248, 249, 250],  // Light background
  white: [255, 255, 255]
}

// Helper functions for consistent styling
function addHeader(doc, title, subtitle = null) {
  // Header background
  doc.setFillColor(...COLORS.primary)
  doc.rect(0, 0, 210, 35, 'F')
  
  // Company logo area (placeholder)
  doc.setFillColor(...COLORS.white)
  doc.rect(15, 8, 25, 18, 'F')
  doc.setFontSize(8)
  doc.setTextColor(...COLORS.primary)
  doc.text('LOGO', 27.5, 18, { align: 'center' })
  
  // Title
  doc.setFontSize(24)
  doc.setFont('helvetica', 'bold')
  doc.setTextColor(...COLORS.white)
  doc.text(title, 50, 20)
  
  if (subtitle) {
    doc.setFontSize(12)
    doc.setFont('helvetica', 'normal')
    doc.text(subtitle, 50, 28)
  }
  
  // Reset text color
  doc.setTextColor(...COLORS.text)
}

function addSection(doc, title, y, backgroundColor = COLORS.lightGray) {
  // Section header
  doc.setFillColor(...backgroundColor)
  doc.rect(15, y - 5, 180, 12, 'F')
  
  doc.setFontSize(14)
  doc.setFont('helvetica', 'bold')
  doc.setTextColor(...COLORS.primary)
  doc.text(title, 20, y + 2)
  
  // Reset formatting
  doc.setFont('helvetica', 'normal')
  doc.setTextColor(...COLORS.text)
  
  return y + 15
}

function addKeyValuePair(doc, key, value, x, y, keyWidth = 40) {
  doc.setFontSize(10)
  doc.setFont('helvetica', 'bold')
  doc.text(`${key}:`, x, y)
  
  doc.setFont('helvetica', 'normal')
  doc.text(String(value), x + keyWidth, y)
}

function addMetricCard(doc, title, value, unit, x, y, width = 40, color = COLORS.primary) {
  // Card background
  doc.setFillColor(...COLORS.white)
  doc.rect(x, y, width, 25, 'F')
  doc.setDrawColor(...color)
  doc.setLineWidth(0.5)
  doc.rect(x, y, width, 25, 'S')
  
  // Title
  doc.setFontSize(9)
  doc.setFont('helvetica', 'normal')
  doc.setTextColor(...COLORS.text)
  doc.text(title, x + width/2, y + 8, { align: 'center' })
  
  // Value
  doc.setFontSize(16)
  doc.setFont('helvetica', 'bold')
  doc.setTextColor(...color)
  doc.text(String(value), x + width/2, y + 16, { align: 'center' })
  
  // Unit
  if (unit) {
    doc.setFontSize(8)
    doc.setFont('helvetica', 'normal')
    doc.text(unit, x + width/2, y + 22, { align: 'center' })
  }
  
  // Reset formatting
  doc.setTextColor(...COLORS.text)
}

function addFooter(doc, pageNumber = 1, totalPages = 1) {
  const pageHeight = doc.internal.pageSize.height
  
  // Footer line
  doc.setDrawColor(...COLORS.lightGray)
  doc.setLineWidth(0.5)
  doc.line(15, pageHeight - 15, 195, pageHeight - 15)
  
  // Footer text
  doc.setFontSize(8)
  doc.setTextColor(...COLORS.text)
  doc.text('Generated by Inventory Management System', 15, pageHeight - 8)
  doc.text(`Page ${pageNumber} of ${totalPages}`, 195, pageHeight - 8, { align: 'right' })
  doc.text(new Date().toLocaleString(), 105, pageHeight - 8, { align: 'center' })
}

export function generateReceivingReportPDF(reportData) {
  try {
    const doc = new jsPDF()
    const { purchaseOrder, receivingRecords, summary } = reportData

    if (!purchaseOrder) throw new Error("purchaseOrder missing")
    if (!summary) throw new Error("summary missing")

    console.log("Generating enhanced PDF with:", { purchaseOrder, receivingRecords, summary })

    // Header
    addHeader(doc, 'Receiving Report', `PO #${purchaseOrder.poNumber}`)

    let currentY = 45

    // Purchase Order Details Section
    currentY = addSection(doc, 'Purchase Order Details', currentY)
    
    // Two-column layout for PO details
    addKeyValuePair(doc, 'PO Number', purchaseOrder.poNumber, 20, currentY)
    addKeyValuePair(doc, 'Status', purchaseOrder.status || 'N/A', 120, currentY)
    currentY += 8
    
    addKeyValuePair(doc, 'Supplier', purchaseOrder.supplier?.name || 'N/A', 20, currentY)
    addKeyValuePair(doc, 'Total Value', `$${(purchaseOrder.totalValue || 0).toFixed(2)}`, 120, currentY)
    currentY += 8
    
    addKeyValuePair(doc, 'Expected Date', 
      purchaseOrder.expectedDate ? new Date(purchaseOrder.expectedDate).toLocaleDateString() : 'N/A', 
      20, currentY)
    currentY += 15

    // Summary Metrics Section
    currentY = addSection(doc, 'Summary Metrics', currentY)
    
    // Metric cards in a row
    addMetricCard(doc, 'Total Items', summary.totalItems || 0, 'items', 20, currentY, 35, COLORS.primary)
    addMetricCard(doc, 'Total Ordered', summary.totalOrdered || 0, 'units', 60, currentY, 35, COLORS.secondary)
    addMetricCard(doc, 'Total Received', summary.totalReceived || 0, 'units', 100, currentY, 35, COLORS.success)
    addMetricCard(doc, 'Completion', `${summary.completionPercentage || 0}`, '%', 140, currentY, 35, COLORS.accent)
    
    currentY += 40

    // Items Table
    const itemsData = (purchaseOrder.items || []).map((item) => [
      item.rawMaterial?.name || item.finishedGood?.name || 'N/A',
      item.rawMaterial?.sku || item.finishedGood?.sku || 'N/A',
      item.quantity || 0,
      item.received || 0,
      `$${(item.unitCost || 0).toFixed(2)}`,
      `$${(item.totalCost || 0).toFixed(2)}`,
    ])

    if (itemsData.length) {
      currentY = addSection(doc, 'Order Items', currentY)
      
      autoTable(doc, {
        head: [['Item Name', 'SKU', 'Ordered', 'Received', 'Unit Cost', 'Total Cost']],
        body: itemsData,
        startY: currentY,
        theme: 'grid',
        styles: { 
          fontSize: 9,
          cellPadding: 4,
          textColor: COLORS.text
        },
        headStyles: { 
          fillColor: COLORS.primary,
          textColor: COLORS.white,
          fontStyle: 'bold',
          fontSize: 10
        },
        alternateRowStyles: {
          fillColor: COLORS.lightGray
        },
        columnStyles: {
          0: { cellWidth: 50 },
          1: { cellWidth: 30 },
          2: { cellWidth: 25, halign: 'center' },
          3: { cellWidth: 25, halign: 'center' },
          4: { cellWidth: 25, halign: 'right' },
          5: { cellWidth: 25, halign: 'right' }
        }
      })
      
      currentY = doc.lastAutoTable.finalY + 15
    }

    // Receiving History
    if (Array.isArray(receivingRecords) && receivingRecords.length > 0) {
      // Check if we need a new page
      if (currentY > 220) {
        doc.addPage()
        addHeader(doc, 'Receiving Report', `PO #${purchaseOrder.poNumber} (continued)`)
        currentY = 45
      }
      
      currentY = addSection(doc, 'Receiving History', currentY)

      const historyData = receivingRecords.map((record) => [
        record.rawMaterial?.name || record.finishedGood?.name || 'N/A',
        record.quantity || 0,
        record.user?.name || 'N/A',
        record.createdAt ? new Date(record.createdAt).toLocaleDateString() : 'N/A',
        record.notes || '-',
      ])

      autoTable(doc, {
        head: [['Item', 'Quantity', 'Received By', 'Date', 'Notes']],
        body: historyData,
        startY: currentY,
        theme: 'grid',
        styles: { 
          fontSize: 9,
          cellPadding: 4,
          textColor: COLORS.text
        },
        headStyles: { 
          fillColor: COLORS.success,
          textColor: COLORS.white,
          fontStyle: 'bold',
          fontSize: 10
        },
        alternateRowStyles: {
          fillColor: COLORS.lightGray
        },
        columnStyles: {
          0: { cellWidth: 60 },
          1: { cellWidth: 25, halign: 'center' },
          2: { cellWidth: 35 },
          3: { cellWidth: 25, halign: 'center' },
          4: { cellWidth: 35 }
        }
      })
    }

    // Add footer
    addFooter(doc)

    return doc
  } catch (err) {
    console.error("❌ PDF generation failed:", err)
    throw err
  }
}

export function generateOutboundReportPDF(reportData) {
  const doc = new jsPDF()
  const { salesOrders, outboundShipments, summary } = reportData

  // Header
  const dateRange = `${reportData.filters?.startDate || 'All time'} - ${reportData.filters?.endDate || 'Present'}`
  addHeader(doc, 'Outbound Report', dateRange)

  let currentY = 45

  // Summary Metrics Section
  currentY = addSection(doc, 'Performance Overview', currentY)
  
  // First row of metrics
  addMetricCard(doc, 'Total Orders', summary.totalOrders, 'orders', 20, currentY, 40, COLORS.primary)
  addMetricCard(doc, 'Total Value', `$${summary.totalValue.toFixed(0)}`, '', 65, currentY, 40, COLORS.success)
  addMetricCard(doc, 'Total Items', summary.totalItems, 'items', 110, currentY, 40, COLORS.secondary)
  
  currentY += 35

  // Status breakdown section
  currentY = addSection(doc, 'Order Status Distribution', currentY)
  
  const statusData = summary.statusBreakdown || {}
  addMetricCard(doc, 'Preparing', statusData.preparing || 0, '', 20, currentY, 35, COLORS.warning)
  addMetricCard(doc, 'Ready', statusData.ready || 0, '', 60, currentY, 35, COLORS.accent)
  addMetricCard(doc, 'Shipped', statusData.shipped || 0, '', 100, currentY, 35, COLORS.primary)
  addMetricCard(doc, 'Delivered', statusData.delivered || 0, '', 140, currentY, 35, COLORS.success)
  
  currentY += 40

  // Sales Orders Table
  if (salesOrders?.length > 0) {
    currentY = addSection(doc, 'Sales Orders', currentY)

    const salesData = salesOrders.map((order) => [
      order.soNumber,
      order.customer?.name || 'N/A',
      order.orderDate ? new Date(order.orderDate).toLocaleDateString() : 'N/A',
      order.status,
      order.items?.length || 0,
      `$${(order.totalValue || 0).toFixed(2)}`,
    ])

    autoTable(doc, {
      head: [['SO Number', 'Customer', 'Order Date', 'Status', 'Items', 'Total Value']],
      body: salesData,
      startY: currentY,
      theme: 'grid',
      styles: { 
        fontSize: 9,
        cellPadding: 4,
        textColor: COLORS.text
      },
      headStyles: { 
        fillColor: COLORS.primary,
        textColor: COLORS.white,
        fontStyle: 'bold',
        fontSize: 10
      },
      alternateRowStyles: {
        fillColor: COLORS.lightGray
      },
      columnStyles: {
        0: { cellWidth: 30 },
        1: { cellWidth: 45 },
        2: { cellWidth: 25, halign: 'center' },
        3: { cellWidth: 25, halign: 'center' },
        4: { cellWidth: 20, halign: 'center' },
        5: { cellWidth: 25, halign: 'right' }
      }
    })

    currentY = doc.lastAutoTable.finalY + 15
  }

  // Outbound Shipments Table
  if (outboundShipments?.length > 0) {
    // Check if we need a new page
    if (currentY > 220) {
      doc.addPage()
      addHeader(doc, 'Outbound Report', `${dateRange} (continued)`)
      currentY = 45
    }
    
    currentY = addSection(doc, 'Direct Shipments', currentY)

    const shipmentData = outboundShipments.map((shipment) => [
      shipment.shipmentNumber,
      shipment.customer?.name || 'N/A',
      shipment.shipDate ? new Date(shipment.shipDate).toLocaleDateString() : 'N/A',
      shipment.status,
      shipment.items?.length || 0,
      shipment.createdBy?.name || 'N/A',
    ])

    autoTable(doc, {
      head: [['Shipment Number', 'Customer', 'Ship Date', 'Status', 'Items', 'Created By']],
      body: shipmentData,
      startY: currentY,
      theme: 'grid',
      styles: { 
        fontSize: 9,
        cellPadding: 4,
        textColor: COLORS.text
      },
      headStyles: { 
        fillColor: COLORS.success,
        textColor: COLORS.white,
        fontStyle: 'bold',
        fontSize: 10
      },
      alternateRowStyles: {
        fillColor: COLORS.lightGray
      },
      columnStyles: {
        0: { cellWidth: 35 },
        1: { cellWidth: 40 },
        2: { cellWidth: 25, halign: 'center' },
        3: { cellWidth: 25, halign: 'center' },
        4: { cellWidth: 20, halign: 'center' },
        5: { cellWidth: 35 }
      }
    })
  }

  // Add footer
  addFooter(doc)

  return doc
}

export function generateProductionReportPDF(reportData) {
  const doc = new jsPDF()
  const { productionOrders, summary } = reportData

  // Header
  const dateRange = `${reportData.filters?.startDate || 'All time'} - ${reportData.filters?.endDate || 'Present'}`
  addHeader(doc, 'Production Report', dateRange)

  let currentY = 45

  // Summary Metrics Section
  currentY = addSection(doc, 'Production Overview', currentY)
  
  // First row of metrics
  addMetricCard(doc, 'Total Orders', summary.totalOrders, 'orders', 20, currentY, 40, COLORS.primary)
  addMetricCard(doc, 'Target Qty', summary.totalTargetQuantity, 'units', 65, currentY, 40, COLORS.secondary)
  addMetricCard(doc, 'Produced', summary.totalProducedQuantity, 'units', 110, currentY, 40, COLORS.success)
  addMetricCard(doc, 'Efficiency', `${summary.averageEfficiency}`, '%', 155, currentY, 35, COLORS.accent)
  
  currentY += 35

  // Status breakdown section
  currentY = addSection(doc, 'Production Status', currentY)
  
  const statusData = summary.statusBreakdown || {}
  addMetricCard(doc, 'Pending', statusData.pending || 0, '', 35, currentY, 40, COLORS.warning)
  addMetricCard(doc, 'In Progress', statusData.inProgress || 0, '', 80, currentY, 40, COLORS.accent)
  addMetricCard(doc, 'Completed', statusData.completed || 0, '', 125, currentY, 40, COLORS.success)
  
  currentY += 45

  // Production Orders Table
  currentY = addSection(doc, 'Production Orders', currentY)

  const productionData = productionOrders.map((order) => [
    order.productionNumber,
    order.finishedGood?.name || 'N/A',
    order.targetQuantity || 0,
    order.producedQuantity || 0,
    order.status,
    order.startDate ? new Date(order.startDate).toLocaleDateString() : '-',
    order.endDate ? new Date(order.endDate).toLocaleDateString() : '-',
  ])

  autoTable(doc, {
    head: [['Production #', 'Product', 'Target', 'Produced', 'Status', 'Start Date', 'End Date']],
    body: productionData,
    startY: currentY,
    theme: 'grid',
    styles: { 
      fontSize: 9,
      cellPadding: 4,
      textColor: COLORS.text
    },
    headStyles: { 
      fillColor: COLORS.primary,
      textColor: COLORS.white,
      fontStyle: 'bold',
      fontSize: 10
    },
    alternateRowStyles: {
      fillColor: COLORS.lightGray
    },
    columnStyles: {
      0: { cellWidth: 28 },
      1: { cellWidth: 45 },
      2: { cellWidth: 20, halign: 'center' },
      3: { cellWidth: 22, halign: 'center' },
      4: { cellWidth: 25, halign: 'center' },
      5: { cellWidth: 25, halign: 'center' },
      6: { cellWidth: 25, halign: 'center' }
    }
  })

  currentY = doc.lastAutoTable.finalY + 15

  // Product Breakdown
  if (summary.productBreakdown?.length > 0) {
    // Check if we need a new page
    if (currentY > 220) {
      doc.addPage()
      addHeader(doc, 'Production Report', `${dateRange} (continued)`)
      currentY = 45
    }
    
    currentY = addSection(doc, 'Product Performance', currentY)

    const productData = summary.productBreakdown
      .filter((product) => product.orderCount > 0)
      .map((product) => [
        product.name,
        product.sku,
        product.orderCount,
        product.totalTarget,
        product.totalProduced,
        `${Math.round(((product.totalProduced || 0) / (product.totalTarget || 1)) * 100)}%`,
      ])

    autoTable(doc, {
      head: [['Product', 'SKU', 'Orders', 'Target', 'Produced', 'Efficiency']],
      body: productData,
      startY: currentY,
      theme: 'grid',
      styles: { 
        fontSize: 9,
        cellPadding: 4,
        textColor: COLORS.text
      },
      headStyles: { 
        fillColor: COLORS.success,
        textColor: COLORS.white,
        fontStyle: 'bold',
        fontSize: 10
      },
      alternateRowStyles: {
        fillColor: COLORS.lightGray
      },
      columnStyles: {
        0: { cellWidth: 50 },
        1: { cellWidth: 25 },
        2: { cellWidth: 20, halign: 'center' },
        3: { cellWidth: 25, halign: 'center' },
        4: { cellWidth: 25, halign: 'center' },
        5: { cellWidth: 25, halign: 'center' }
      }
    })
  }

  // Add footer
  addFooter(doc)

  return doc
}